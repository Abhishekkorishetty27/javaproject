package com.xworkz.today.Practice;

public class Car {
	
	  
	    String make;
	    String model;
	    int year;
	    String color;
	    double price;

	   
	    public Car() {
	     
	    }

	    public Car(String make) {
	        System.out.println(this.make = make);
	      
	    }

	    public Car(String make, String model) {
	    	System.out.println(this.make = make);
	    	System.out.println(this.model = model);
	       
	    }

	    public Car(String make, String model, int year) {
	    	System.out.println(this.make = make);
	    	System.out.println(this.model = model);
	    	System.out.println(this.year = year);
	       
	    }

	    public Car(String make, String model, int year, String color) {
	    	System.out.println(this.make = make);
	    	System.out.println(this.model = model);
	    	System.out.println(this.year = year);
	    	System.out.println(this.color = color);
	    
	    }

	    public Car(String make, String model, int year, String color, double price) {
	    	System.out.println(this.make = make);
	    	System.out.println(this.model = model);
	    	System.out.println(this.year = year);
	    	System.out.println(this.color = color);
	    	System.out.println(this.price = price);
	    }
	}


